/* 11. Splice Element
Define function spliceElement(someArr, index)
Splice the element at the provided index in the array.
*/
function spliceElement(someArr, index) {
    //remove the element at index in someArr
    someArr.splice(index, 1)
    //console.log(someArr);
    
}
